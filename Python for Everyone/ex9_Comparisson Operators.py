x=5
# == Means: Equal to
if x == 5:
    print("Equals 5")
# > Means: Greater than   
if x > 4:
    print("Greater than 4")
# >= Means: Greater than or Equal to
if x >= 5:
    print("Greater than or Equals 5") 
# < Means: Less than
if x < 6:
    print("Less than 6")
# <= Means: Less than or Equal to
if x <= 5:
    print("Less than or Equals 5")     
# != Means: Not Equal
if x != 6:
    print("Not Equal 6")